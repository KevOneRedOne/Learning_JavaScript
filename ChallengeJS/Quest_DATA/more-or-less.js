// creation of a funtion which return the argument + one
function more (a) {
    return a + 1;
}

// creation of a funtion which return the argument - one
function less (a) {
    return a - 1;
}
// funtion that takes two arguments and add them 
function add (a,b) {
    return a + b;
}
// funtion that takes two arguments and substracts them 
function sub (a,b) {
    return a - b;
}
